package eu.impress.logevo.model;

public class StatsScoring {
	String etd;
	String gcs;
	String rts;
	String code;
	public String getEtd() {
		return etd;
	}
	public void setEtd(String etd) {
		this.etd = etd;
	}
	public String getGcs() {
		return gcs;
	}
	public void setGcs(String gcs) {
		this.gcs = gcs;
	}
	public String getRts() {
		return rts;
	}
	public void setRts(String rts) {
		this.rts = rts;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}

}
